//! type conversion

console.log("10" +"10")
console.log(10+"10")
console.log(true + "10")
console.log(null +"hello")
console.log(undefined +"hello")

console.log(10+10)
console.log(10 + true)
console.log(-10 + false)
console.log(10 + null)
console.log(10 + undefined)

console.log(true + true)
