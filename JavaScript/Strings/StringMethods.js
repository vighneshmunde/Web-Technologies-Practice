// String Methods
let language = " Web Technology "
console.log(language)
console.log(language.length)

// CharAt()
console.log(language.charAt(5))

// charCodeAt()
console.log(language.charCodeAt(1))

// includes()
console.log(language.includes("web"))

// toUpperCase()
console.log(language.toUpperCase())

// toLowerCase()
console.log(language.toLowerCase())

// trimStart()
console.log(language.trimStart())

// trimEnd()
console.log(language.trimEnd())

// trim()
console.log(language.trim())

// split()
let newArr = language.split()
console.log(newArr)